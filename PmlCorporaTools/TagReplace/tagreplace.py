import re
import os

def apply_regex_file(filename, compiled):
	with open(filename) as f:
		data = f.read()

	count = 0
	for search_pattern, replacement in compiled:
		matches = search_pattern.findall(data)
		if matches:
			count += len(matches)
			data = search_pattern.sub(replacement, data)

	if count:
		with open(filename, 'w') as f:			
			f.write(data)
	return count

def apply_regex_folder(root, compiled):
	count = 0
	for path, dirs, files in os.walk(root):
		for filename in files:
			if not filename.endswith('.m'):
				continue
			fullname = os.path.join(path, filename)
			
			count += apply_regex_file(fullname, compiled)
	print(f'{count} changes made!')

